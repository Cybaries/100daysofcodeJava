public class App {
    public static void main(String[] args) throws Exception {
        //Variable declaration
        int a = 9, b = 10;

        //Array declaration
        int[] arr = { 1, 2, 3, 4 };

        //Printing
        System.out.println(a + " " + b + " " + arr.length);

        //Strings
        String s = "Hello!";
        System.out.println(s);

        //boolean
        boolean H = 1 == 0;
        System.out.println(H);

        //Typecasting
        int hello = (int) 11.5;
        System.out.println(hello);
        
    }
}
